            <h2 class="blog-post-title mb-3">¿Que es la COVID-19?</h2>
            <hr>
            <p>La enfermedad por coronavirus de 2019, más conocida como COVID-19, covid-19 o covid, e incorrectamente llamada neumonía por coronavirus, coronavirusnota o corona, es una enfermedad infecciosa causada por el SARS-CoV-2.</p>
            
            <p>Produce síntomas que incluyen fiebre, tos, disnea (dificultad respiratoria), mialgia (dolor muscular) y fatiga. En casos graves se caracteriza por producir neumonía, síndrome de dificultad respiratoria aguda,sepsis y choque circulatorio. El choque séptico es la forma más común en estos casos, pero los otros tipos también pueden ocurrir. Por ejemplo, un choque obstructivo puede ser el resultado de una embolia pulmonar, complicación también posible. Según la OMS, la infección es mortal entre el 0,5 % y el 1 % de los casos.Se han autorizado varios tratamientos antivirales por la FDA como pueden ser el paxlovid, remdesivir o molnupiravir en pacientes con un curso de la enfermedad de leve a moderado, con factores de riesgos en los que se quiera evitar una evolución grave de la enfermedad.</p>  
            
            <p>La transmisión del SARS-CoV-2 se produce mediante pequeñas gotas —microgotas de Flügge— que se emiten al hablar, estornudar, toser o espirar, que al ser despedidas por un portador (que puede no tener síntomas de la enfermedad o estar incubándola) pasan directamente a otra persona mediante la inhalación, o quedan sobre los objetos y superficies que rodean al emisor, y luego, a través de las manos, que lo recogen del ambiente contaminado, toman contacto con las membranas mucosas orales, nasales y oculares, al tocarse la boca, la nariz o los ojos. También está documentada la transmisión por aerosoles ( < 5μm). La propagación mediante superficies contaminadas o fómites (cualquier objeto carente de vida, o sustancia, que si se contamina con algún patógeno es capaz de transferirlo de un individuo a otro) no contribuye sustancialmente a nuevas infecciones.</p>
